@dataclass(frozen=True)
class FactorSpec:
    paper: str
    formula_id: str
    required_columns: tuple[str, ...]
    output_role: str
    strict_supported: bool
    alternative_required_columns: tuple[tuple[str, ...], ...] = ()
    # Provenance: who wrote the implementation
    #   "ours"              — implemented by us from scratch (HFT signals, current default)
    #   "crosssection"      — adapted from OpenSourceAP/CrossSection Python code
    #   "evolved"           — generated by Phase 2 evolution system (no direct paper)
    origin: str = "ours"
    # For evolved factors: comma-separated parent factor names
    parent_factors: str = ""


