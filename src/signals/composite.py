"""
Strict paper-backed composite alpha construction.

Default feature construction is intentionally conservative: only factors with
an explicit formula, a declared paper source, and satisfied input fields are
included in the directional alpha bank. Labels, gates, and diagnostics remain
available by explicit selection, but they are not silently mixed into the
default composite.
"""

